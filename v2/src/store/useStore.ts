/**
 * IndexedDB storage — يحل مشكلة فقدان البيانات على Android
 * localStorage محدودة بـ 5-10MB وتُمسح أحياناً، بينما IndexedDB تدعم مئات الميغابايت
 */
function createIndexedDBStorage(): PersistStorage<any> {
  const DB_NAME = 'gt-sararim-db';
  const STORE_NAME = 'state';
  // نفتح الاتصال مرة واحدة ونعيد استخدامه — يمنع تعارض الاتصالات على Android
  let dbPromise: Promise<IDBDatabase> | null = null;

  function getDB(): Promise<IDBDatabase> {
    if (!dbPromise) {
      dbPromise = new Promise((resolve, reject) => {
        const req = indexedDB.open(DB_NAME, 1);
        req.onupgradeneeded = () => {
          req.result.createObjectStore(STORE_NAME);
        };
        req.onsuccess = () => resolve(req.result);
        req.onerror = () => {
          dbPromise = null; // أعد المحاولة في الطلب التالي
          reject(req.error);
        };
        req.onblocked = () => {
          dbPromise = null;
          reject(new Error('IndexedDB blocked'));
        };
      });
    }
    return dbPromise;
  }

  return {
    getItem: async (name: string): Promise<StorageValue<any> | null> => {
      try {
        const db = await getDB();
        return new Promise((resolve) => {
          const tx = db.transaction(STORE_NAME, 'readonly');
          const req = tx.objectStore(STORE_NAME).get(name);
          req.onsuccess = () => resolve(req.result ?? null);
          req.onerror = () => resolve(null);
        });
      } catch {
        // fallback: قراءة من localStorage
        try {
          const s = localStorage.getItem(name);
          return s ? JSON.parse(s) : null;
        } catch { return null; }
      }
    },
    setItem: async (name: string, value: StorageValue<any>): Promise<void> => {
      try {
        const db = await getDB();
        await new Promise<void>((resolve, reject) => {
          const tx = db.transaction(STORE_NAME, 'readwrite');
          tx.objectStore(STORE_NAME).put(value, name);
          tx.oncomplete = () => resolve();
          tx.onerror = () => reject(tx.error);
          tx.onabort = () => reject(new Error('Transaction aborted'));
        });
        // نسخة احتياطية في localStorage للبيانات الخفيفة (< 3MB)
        try {
          const str = JSON.stringify(value);
          if (str.length < 3_000_000) localStorage.setItem(name, str);
        } catch { /* تجاهل إذا امتلأ localStorage */ }
      } catch {
        // fallback: حفظ في localStorage مباشرة
        try { localStorage.setItem(name, JSON.stringify(value)); } catch { /* تجاهل */ }
      }
    },
    removeItem: async (name: string): Promise<void> => {
      try {
        const db = await getDB();
        await new Promise<void>((resolve) => {
          const tx = db.transaction(STORE_NAME, 'readwrite');
          tx.objectStore(STORE_NAME).delete(name);
          tx.oncomplete = () => resolve();
          tx.onerror = () => resolve();
        });
      } catch {
        try { localStorage.removeItem(name); } catch { /* ignore */ }
      }
    },
  };
}

